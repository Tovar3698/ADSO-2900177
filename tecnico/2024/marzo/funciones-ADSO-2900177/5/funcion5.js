/**
 * Función para sacar el promedio de 3 números
 * Autor: Javier Ricardo Tovar T.
 * Fecha: Lunes 01 de abril de 2024
*/

function prom(pnumUno,pnumDos,pnumTre) {
    let numUno = pnumUno;
    let numDos = pnumDos;
    let numTre = pnumTre;
    let prom;
    prom = (numUno + numDos + numTre) / 3;
    return prom;
}